//                                               Массивы

// Чтобы создать массив необходимо создать переменную
// В массивах отсчет начинается с 0, поэтому первый элемент по индексу будет равен 0, второй - 1 и так далее.
// В js нет строгого типизирования и массивы можно заполнять любыми типами данных вперемешку, как и в Руби

var some = new Array(); // Создание пустого массива
some[0] = '1'; // Добавление элемента
some[1] = 2;
console.log(some[0]); // Вывод первого элемента

var some2 = []; // упрощенное создание массива

var array = new Array(1, 5, 2); // Создание массива со значениями сразу же

var arr = [5, true, 'stroka', 5.7, 0, -100]; // упрощенное создание массива со значениями сразу же
console.log(arr); // массив выводится в строку так же можно его развернуть за доп инфой
arr[3] = 'word'; // изменяем элемент массива
console.log(arr[3]); //=> word


//                                            Методы массивов

